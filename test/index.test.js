import { calculate } from "../dist/index.js";

const tests = [
  ["1 + 2", "3"],
  ["2 - 4", "-2"],
  ["1 * 2", "2"],
  ["1 / 2", "0.5"],
  ["2 ^ 3", "8"],
  ["3 % 2", "1"],
  ["3 * 2 ^ 2", "12"],
  ["-(1 + 2)", "-3"],
  ["0b0101 + 0b1010", "0b1111"],
  ["0x05 + 0x0a", "0x0f"],
  ["0.1 + 0.2", "0.3"],
  ["2pi", "6.28318530717959"],
  ["sum(1,2,3)", "6"],
  ["max(1,2,3)", "3"],
  ["sin(30)", "0.5"],
  ["prime(153)", '"3 ^ 2 * 17"'],
];

test.each(tests)("%s = %s", (a, b) => {
  expect(calculate(a)).toBe(b);
});
